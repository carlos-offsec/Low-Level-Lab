main:
; 1. ZERAR OS SEGMENTOS 
xor ax, ax
mov ds, ax

; 2. INICIALIZAR A PILHA 
mov ss, ax         ; SS = 0
mov sp, 0x7C00     ; A pilha cresce para baixo do bootloader 
mov bp, sp

; 3. SALVAR O DRIVE
mov [driveNum], dl 

; 4. CONFIGURAR O ES PARA A LEITURA DO DISCO
; carregar a parte 2 em ES:BX = 0x07C0:stage2 (Físico: 0x7E00)
mov ax, 0x07C0
mov es, ax

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

;Gerar o modo de video 13  320x200 256 color graphics A000 (MCGA,VGA)
xor ax, ax
mov al, 0x13
int 0x10

call DesenhaFundoPreto				

call ChamaParte2

jmp $

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

DesenhaFundoPreto:
push bp
mov bp, sp
pusha

;INT 10,C - Grava um pixel gráfico na coordenada
;    AH = 0C
;    AL = valor da cor (submetido a XOR com o pixel atual se o bit 7 for 1)
;    BH = número da página
;    CX = número da coluna (contagem a partir de zero)
;    DX = número da linha (contagem a partir de zero)

xor cx, cx
xor dx, dx
mov ah, 0x0c
mov al, 0x0f
recomeca:
int 0x10
inc cx
cmp cx, 0x140 ;320 em Decimal
je zeraHorizontal
jmp recomeca

fim:
popa
mov sp, bp
pop bp
ret

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

zeraHorizontal:
xor cx, cx
inc dx
cmp dx, 0xc8 ;200 em Decimal
je fim 
jmp recomeca

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

ChamaParte2:

;INT 13,2 - Setores de Disco de Leitura
;	AH = 02
;	AL = número de setores a serem lidos (1-128 dez.)
;	CH = número da pista/cilindro (0-1023 dec.)
;	CL = número do setor (1-17 de dezembro)
;	DH = número da cabeça (0-15 de dezembro)
;	DL = número da unidade (0=A:, 1=2o disquete, 80h=unidade 0, 81h=unidade 1)
;	ES:BX = ponteiro para buffer

;	no retorno:
;	AH = status (ver INT 13, STATUS)
;	AL = número de setores lidos
;	CF = 0 se for bem-sucedido
;	   = 1 se erro

mov ah, 2
mov al, 5
mov ch, 0
mov cl, 2
mov dh, 0
mov dl, [driveNum]
mov bx, stage2
int 0x13
jmp stage2

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

driveNum: db 0

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

;finalizando
times(510 - ($ - $$ )) db 0x00
dw 0xAA55

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
;--------------------------------COMECA A PARTE 2-----------------------------------------------------------------------------------------------------------------------
;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

stage2:

call desenha

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

final:
jmp $

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

desenha:

;INT 10,C - Grava um pixel gráfico na coordenada
;    AH = 0C
;    AL = valor da cor (submetido a XOR com o pixel atual se o bit 7 for 1)
;    BH = número da página
;    CX = número da coluna (contagem a partir de zero)
;    DX = número da linha (contagem a partir de zero)

push bp
mov bp, sp
pusha

xor ax, ax
mov ds, ax
mov si, ax

push 0x0f		;Cor -> [sp + 0x06]
push 0x00		;X -> valor inicio horizontal -> [sp + 0x04]
push 0xc7		;Y -> valor inicio vertical -> [sp + 0x02]
push 0x00		;Numero de repeticoes -> [sp]

mov di, sp
mov bl, byte [0x7c00 + dados + si] ;Repeticoes

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

inicio:
mov dx, [di + 0x02] ;Y -> valor inicio vertical -> [sp + 0x02]

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

volta1:
xor bh, bh
mov cx, [di + 0x04] ;X -> valor inicio horizontal -> [sp + 0x04]
mov al, [di + 0x06]
mov ah, 0x0c
int 0x10
dec bl ; Diminui uma repeticao
mov [di], bl ; Salva as repeticoes que faltam

cmp bl, 0
je trocaCor

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

volta2:
xor ax, ax
mov ax, word[di + 0x04] ;X -> valor inicio horizontal -> [sp + 0x04]
inc ax
cmp ax, 0x13f ; 319
je preparaRetorno
mov [di + 0x04], ax
jmp volta1

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

preparaRetorno:
xor ax, ax
mov [di + 0x04], ax ;X -> valor inicio horizontal -> [sp + 0x04]
mov ax, [di + 0x02] ;Y -> valor inicio vertical -> [sp + 0x02]
dec ax
mov [di + 0x02], ax
inc si
mov bl, byte [0x7c00 + dados + si] ;Repeticoes
cmp bl, 0xff
je final
jmp inicio

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

trocaCor:
mov al, [di + 0x06]
cmp al, 0x0f
je preto
mov al, 0x0f
jmp salva

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

preto:
mov al, 0x00

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

salva:
mov [di + 0x06], al
inc si

mov bl, byte [0x7c00 + dados + si] ;Repeticoes
cmp bl, 0xff
je final
mov [di], bl
jmp volta2

;-----------------------------------------------------------------------------------------------------------------------------------------------------------------------

popa
mov sp, bp
pop bp
ret

